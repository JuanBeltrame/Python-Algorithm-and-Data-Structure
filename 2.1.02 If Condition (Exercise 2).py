#----------------------------------------------------------------------
# Main Program

# Variables
# numero: entero 

numero = int(input("Ingrese un número: "))

if numero > 0:
    print("\nEl número", numero, "es Positivo")
elif numero < 0:
    print("\nEl número", numero, "es Negativo")
else:
    print("\nIngresaste el cero")





